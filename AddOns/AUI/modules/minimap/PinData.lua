local function AUI_MapPin_GetUnitState(pinTag)
	local groupSize = GetGroupSize()
	
	local unitTag = nil
	for i=1, groupSize do
		unitTag = GetGroupUnitTagByIndex(i)	
		
		if unitTag == pinTag then	
			if IsUnitDead(unitTag) == true then
				return AUI_MINIMAP_UNIT_DEAD, IsUnitFriend(unitTag) 
			elseif IsUnitInCombat(unitTag) == true then
				return AUI_MINIMAP_UNIT_IN_COMBAT, IsUnitFriend(unitTag) 
			end
			break
		end
	end

	return AUI_MINIMAP_UNIT_NORMAL, IsUnitFriend(unitTag) 
end

function AUI.Minimap.Pin.GetGroupStatePinType(groupTag)
	local pinType = MAP_PIN_TYPE_GROUP
			
	if groupTag then
		local isLeader = IsUnitGroupLeader(groupTag)
		local unitState, isFriend = AUI_MapPin_GetUnitState(groupTag)

		if isLeader then
			if unitState == AUI_MINIMAP_UNIT_IN_COMBAT and AUI.Settings.Minimap.showGroupMemberStatus then	
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_LEADER_FRIEND_IN_COMBAT				
				else					
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_LEADER_IN_COMBAT
				end
			elseif unitState == AUI_MINIMAP_UNIT_DEAD and AUI.Settings.Minimap.showGroupMemberStatus then	
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_FRIEND_DEAD				
				else					
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_DEAD
				end
			elseif unitState == AUI_MINIMAP_UNIT_NORMAL or not AUI.Settings.Minimap.showGroupMemberStatus then
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_LEADER_FRIEND				
				else					
					pinType = MAP_PIN_TYPE_GROUP_LEADER
				end					
			end
		else
			if unitState == AUI_MINIMAP_UNIT_IN_COMBAT and AUI.Settings.Minimap.showGroupMemberStatus then	
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_FRIEND_IN_COMBAT				
				else					
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_IN_COMBAT
				end
			elseif unitState == AUI_MINIMAP_UNIT_DEAD and AUI.Settings.Minimap.showGroupMemberStatus then	
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_FRIEND_DEAD				
				else					
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_DEAD
				end									
			elseif unitState == AUI_MINIMAP_UNIT_NORMAL or not AUI.Settings.Minimap.showGroupMemberStatus then
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_FRIEND				
				else					
					pinType = MAP_PIN_TYPE_GROUP
				end					
			end
		end
	end

	return pinType
end

local function AUI_Minimap_SetIconThemeData(iconThemeData, pinData)
	if pinData and iconThemeData then	
		local overlayTexture = iconThemeData["Overlay_Texture"]
		local pulseTexture = iconThemeData["Pulse_Texture"]
		local backgroundTexture = iconThemeData["Background_Texture"]	
			
		if overlayTexture and pulseTexture and backgroundTexture then		
			pinData.texture = function() return overlayTexture, pulseTexture, backgroundTexture end
		elseif overlayTexture and backgroundTexture and not pulseTexture then
			pinData.texture = function() return overlayTexture, nil, backgroundTexture end
		elseif overlayTexture and pulseTexture and not backgroundTexture then
			pinData.texture = function() return overlayTexture, pulseTexture, nil end	
		elseif overlayTexture then
			pinData.texture = function() return overlayTexture, nil, nil end		
		end	
			
		local color = iconThemeData["Color"]
	
		if color then
			pinData.tint = AUI.Color.ConvertHexToRGBA(color)
		end
	
		local isAnimated = iconThemeData["IsAnimated"]
	
		if color then
			pinData.isAnimated = isAnimated
		end
	end			
end

