local function Add()
	-- Add the design theme
	AUI.Minimap.Theme.AddMinimapTheme("emm", "EMM", themeData)		
		
	-- Add the icon theme
	AUI.Minimap.Theme.AddMinimapIconTheme("emm", "EMM", iconThemeData)	
end										
	
--[[----------------------------------------------------------
	Do not change
  ]]----------------------------------------------------------	
  
local function OnLoad(p_eventCode, p_addOnName)
	if p_addOnName ~= addonName then
        return
    end	
	
	if AUI then
		Add()
	end

	EVENT_MANAGER:UnregisterForEvent(addonName .. "_THEME_EVENT_ADD_ON_LOADED", EVENT_ADD_ON_LOADED)	
end

EVENT_MANAGER:RegisterForEvent(addonName .. "_THEME_EVENT_ADD_ON_LOADED", EVENT_ADD_ON_LOADED, OnLoad)

